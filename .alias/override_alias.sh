#!/usr/bin/env bash

## Colorize the grep command output for ease of use (good for log files)##
alias grep='grep --color=auto'
alias egrep='egrep --color=auto'
alias fgrep='fgrep --color=auto'

# Utils
alias wget='wget -c'

## Override top with htop ##
alias top='htop'

## Untar ##
alias tgz='tar -xvfz'